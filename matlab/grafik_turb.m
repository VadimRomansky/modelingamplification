clear;
load turb_mid.dat;
load turb_FEB.dat;
load turb_zero.dat;
N=80;
turbm(1:N,1:7)=0;
turbf(1:N,1:7)=0;
turbz(1:N,1:7)=0;
for j=1:N,
    turbm(j,1)=turb_mid(2*j-1,4);
    turbm(j,2)=turb_mid(11*2*N+2*j-1,5);
    turbm(j,3)=turb_mid(12*2*N+2*j-1,5);
    turbm(j,4)=turb_mid(13*2*N+2*j-1,5);
    turbm(j,5)=turb_mid(14*2*N+2*j-1,5);
    turbm(j,6)=turb_mid(15*2*N+2*j-1,5);
    turbm(j,7)=turb_mid(15*2*N+2*j-1,5);
end;
for j=1:N,
    turbf(j,1)=turb_FEB(2*j-1,4);
    turbf(j,2)=turb_FEB(11*2*N+2*j-1,5);
    turbf(j,3)=turb_FEB(12*2*N+2*j-1,5);
    turbf(j,4)=turb_FEB(13*2*N+2*j-1,5);
    turbf(j,5)=turb_FEB(14*2*N+2*j-1,5);
    turbf(j,6)=turb_FEB(15*2*N+2*j-1,5);
    turbf(j,7)=turb_FEB(15*2*N+2*j-1,5);
end;
for j=1:N,
    turbz(j,1)=turb_zero(2*j-1,4);
    turbz(j,2)=turb_zero(11*2*N+2*j-1,5);
    turbz(j,3)=turb_zero(12*2*N+2*j-1,5);
    turbz(j,4)=turb_zero(13*2*N+2*j-1,5);
    turbz(j,5)=turb_zero(14*2*N+2*j-1,5);
    turbz(j,6)=turb_zero(15*2*N+2*j-1,5);
    turbz(j,7)=turb_zero(15*2*N+2*j-1,5);
end;
figure(1);
plot (turbm(1:N,1),turbm(1:N,7));
title ('turb mib');
xlabel ('k');
ylabel ('kW(k)');
grid ;
figure(2);
plot (turbf(1:N,1),turbf(1:N,7));
title ('turb FEB');
xlabel ('k');
ylabel ('kW(k)');
grid ;
figure(3);
plot (turbz(1:N,1),turbz(1:N,7));
title ('turb zero');
xlabel ('k');
ylabel ('kW(k)');
grid ;