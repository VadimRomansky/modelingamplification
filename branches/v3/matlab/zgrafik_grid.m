clear;
load zprofile.dat;
N1=1;
N2=4000;
N3=6;
e = size(zprofile,1)/N2 - 1;
d = fix(3*e/4);
c = fix(e/2);
b = fix(e/4);
a = 5;
vel(1:N2,1:N3 + 1)= 0;
rho(1:N2,1:N3 + 1) = 0;
pressure(1:N2,1:N3 + 1) = 0;
temp(1:N2,1:N3 + 1) = 0;
for j=1:N2,
    vel(j,1)=zprofile(j,1);
    vel(j,2)=zprofile(j,2);
    vel(j,3)=zprofile(a*N2 + j,2);
    vel(j,4)=zprofile(b*N2 + j,2);
    vel(j,5)=zprofile(c*N2 + j,2);
    vel(j,6)=zprofile(d*N2 + j,2);
    vel(j,7)=zprofile(e*N2 + j,2);
    rho(j,1)=zprofile(j,1);
    rho(j,2)=zprofile(j,3);
    rho(j,3)=zprofile(a*N2 + j,3);
    rho(j,4)=zprofile(b*N2 + j,3);
    rho(j,5)=zprofile(c*N2 + j,3);
    rho(j,6)=zprofile(d*N2 + j,3);
    rho(j,7)=zprofile(e*N2 + j,3);
    pressure(j,1)=zprofile(j,1);
    pressure(j,2)=zprofile(j,4);
    pressure(j,3)=zprofile(a*N2 + j,4);
    pressure(j,4)=zprofile(b*N2 + j,4);
    pressure(j,5)=zprofile(c*N2 + j,4);
    pressure(j,6)=zprofile(d*N2 + j,4);
    pressure(j,7)=zprofile(e*N2 + j,4); 
    temp(j,1)=zprofile(j,1);
    temp(j,2)=zprofile(j,5);
    temp(j,3)=zprofile(a*N2 + j,5);
    temp(j,4)=zprofile(b*N2 + j,5);
    temp(j,5)=zprofile(c*N2 + j,5);
    temp(j,6)=zprofile(d*N2 + j,5);
    temp(j,7)=zprofile(e*N2 + j,5); 
end
figure(1);
plot (vel(1:N2,1),vel(1:N2,2),'cyan',vel(1:N2,1),vel(1:N2,3),'green',vel(1:N2,1),vel(1:N2,4),'blue',vel(1:N2,1),vel(1:N2,5),'black',vel(1:N2,1),vel(1:N2,6),'yellow',vel(1:N2,1),vel(1:N2,7),'red');
title ('u(x)');
xlabel ('r cm');
ylabel ('U cm/s');
grid ;
figure(2);
plot (rho(1:N2,1),rho(1:N2,2),'cyan',rho(1:N2,1),rho(1:N2,3),'green',rho(1:N2,1),rho(1:N2,4),'blue',rho(1:N2,1),rho(1:N2,5),'black',rho(1:N2,1),rho(1:N2,6),'yellow',rho(1:N2,1),rho(1:N2,7),'red');
title ('density');
xlabel ('r cm');
ylabel ('rho 10^-5 g/cm^3');
grid ;
figure(3);
plot (pressure(1:N2,1),pressure(1:N2,2),'cyan',pressure(1:N2,1),pressure(1:N2,3),'green',pressure(1:N2,1),pressure(1:N2,4),'blue',pressure(1:N2,1),pressure(1:N2,5),'black',pressure(1:N2,1),rho(1:N2,6),'yellow',pressure(1:N2,1),pressure(1:N2,7),'red');
title ('pressure');
xlabel ('r cm');
ylabel ('pressure');
grid ;
figure(4);
plot (temp(1:N2,1),temp(1:N2,2),'cyan',temp(1:N2,1),temp(1:N2,3),'green',temp(1:N2,1),temp(1:N2,4),'blue',temp(1:N2,1),temp(1:N2,5),'black',temp(1:N2,1),temp(1:N2,6),'yellow',temp(1:N2,1),temp(1:N2,7),'red');
title ('tempersture');
xlabel ('r cm');
ylabel ('T');
grid ;
